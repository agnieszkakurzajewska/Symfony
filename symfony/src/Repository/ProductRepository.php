        $entityManager = $this->getEntityManager();

        $query = $entityManager->createQuery(
            'SELECT p
            FROM App\Entity\Product p
            WHERE p.amount > :amount
            ORDER BY p.amount ASC'
        )->setParameter('amount', $amount);
